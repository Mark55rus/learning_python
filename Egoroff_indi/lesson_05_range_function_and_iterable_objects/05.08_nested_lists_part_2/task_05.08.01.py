""" Побочная диагональ.
    Дана матрица размером NxN, требуется найти максимальное значение среди элементов, расположенных на побочной
диагонали. Под побочной диагональю матрицы подразумевается диагональ, проведённая из правого верхнего угла до левого
нижнего угла.
    Входные данные:
Программа сперва принимает на вход число N (N<=15) - количество строк и столбцов в списке, а затем в N строках записаны
элементы списка.
    Выходные данные:
Вывести самой большой элемент на побочной диагонали"""

matrix = [list(map(int, input().split())) for _ in range(int(input()))]
largest = 0
for row in range(len(matrix)):
    for column in range(len(matrix)):
        if row + column == len(matrix) - 1:
            if matrix[row][column] > largest:
                largest = matrix[row][column]
print(largest)
