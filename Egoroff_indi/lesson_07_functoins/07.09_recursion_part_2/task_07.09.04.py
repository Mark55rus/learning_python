""" Быстрая сортировка (quick sort).
    Быстрая сортировка - еще один вид сортировки, который использует рекурсию.
    Ваша задача реализовать этот алгоритм. Для этого нужно будет создать функцию quick_sort, которая будет принимать и
сходный список и возвращать новый отсортированный в порядке неубывания список.
    Необходимо написать только определение функций quick_sort, при этом нельзя пользоваться встроенными сортировками в
Python"""


def quick_sort(lst):
    """
    Рекурсивная функция быстрой сортировки для списка lst.
    """
    # Базовый случай - список содержит 0 или 1 элементов
    if len(lst) <= 1:
        return lst

    # Выбираем опорный элемент и разбиваем список на две части
    pivot = lst[0]
    left = []
    right = []
    for elem in lst[1:]:
        if elem <= pivot:
            left.append(elem)
        else:
            right.append(elem)

    # Рекурсивно сортируем левую и правую части списка
    left_sorted = quick_sort(left)
    right_sorted = quick_sort(right)

    # Собираем отсортированный список из левой части, опорного элемента и правой части
    return left_sorted + [pivot] + right_sorted
