def is_palindrome(text):
    string_alphabet = ''
    for c in text:
        if c.isalpha():
            string_alphabet += c.lower()
    if string_alphabet == string_alphabet[::-1]:
        return True
    else:
        return False


print(is_palindrome(input()))
