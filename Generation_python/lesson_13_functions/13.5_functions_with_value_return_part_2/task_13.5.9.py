def is_correct_bracket(text):
    start = '('
    end = ')'
    while len(text) > 0:
        if start in text and end in text:
            if text[0] == start:
                text = text.replace(start, '', 1)
                text = text.replace(end, '', 1)
            else:
                return False
        else:
            return False
    else:
        return True


